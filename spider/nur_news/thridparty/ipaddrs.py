    response = requests.post(url=url)
    res = json.loads(response.text)
    if res['status'] == 0:
        province = res['content']['address_detail']['province']
        # ciry = res['content']['address_detail']['city']
        if '新疆' in province:
            return 'good_user'
        else:
            return 'bad_user'
    else:
        return 'no data'


